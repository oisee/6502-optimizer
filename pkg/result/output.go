package result

import (
	"encoding/json"
	"fmt"
	"io"

	"github.com/oisee/6502-optimizer/pkg/inst"
)

// JSONRule is the serializable form of a Rule.
type JSONRule struct {
	Source      []string `json:"source"`
	Replacement []string `json:"replacement"`
	BytesSaved  int      `json:"bytes_saved"`
	CyclesSaved int      `json:"cycles_saved"`
	DeadFlags   string   `json:"dead_flags,omitempty"`
}

// WriteJSON serializes rules to JSON.
func WriteJSON(w io.Writer, rules []Rule) error {
	jrules := make([]JSONRule, len(rules))
	for i, r := range rules {
		jrules[i] = JSONRule{
			Source:      disasmSeq(r.Source),
			Replacement: disasmSeq(r.Replacement),
			BytesSaved:  r.BytesSaved,
			CyclesSaved: r.CyclesSaved,
			DeadFlags:   DeadFlagDesc(r.DeadFlags),
		}
	}
	enc := json.NewEncoder(w)
	enc.SetIndent("", "  ")
	return enc.Encode(jrules)
}

// ReadJSON loads rules from JSON.
func ReadJSON(r io.Reader) ([]JSONRule, error) {
	var jrules []JSONRule
	if err := json.NewDecoder(r).Decode(&jrules); err != nil {
		return nil, err
	}
	return jrules, nil
}

// WriteGoCode generates Go peephole optimizer code.
func WriteGoCode(w io.Writer, rules []Rule) error {
	fmt.Fprintf(w, "package peephole\n\n")
	fmt.Fprintf(w, "// Generated by 6502opt. DO NOT EDIT.\n\n")
	fmt.Fprintf(w, "type PeepholeRule struct {\n")
	fmt.Fprintf(w, "\tSource      []string\n")
	fmt.Fprintf(w, "\tReplacement []string\n")
	fmt.Fprintf(w, "\tBytesSaved  int\n")
	fmt.Fprintf(w, "\tCyclesSaved int\n")
	fmt.Fprintf(w, "\tDeadFlags   string\n")
	fmt.Fprintf(w, "}\n\n")
	fmt.Fprintf(w, "var Rules = []PeepholeRule{\n")
	for _, r := range rules {
		fmt.Fprintf(w, "\t// %s -> %s (-%d bytes, -%d cycles",
			joinSeq(r.Source), joinSeq(r.Replacement), r.BytesSaved, r.CyclesSaved)
		if r.DeadFlags != 0 {
			fmt.Fprintf(w, ", dead: %s", DeadFlagDesc(r.DeadFlags))
		}
		fmt.Fprintf(w, ")\n")
		fmt.Fprintf(w, "\t{Source: %s, Replacement: %s, BytesSaved: %d, CyclesSaved: %d",
			goStrSlice(r.Source), goStrSlice(r.Replacement), r.BytesSaved, r.CyclesSaved)
		if r.DeadFlags != 0 {
			fmt.Fprintf(w, ", DeadFlags: %q", DeadFlagDesc(r.DeadFlags))
		}
		fmt.Fprintf(w, "},\n")
	}
	fmt.Fprintf(w, "}\n")
	return nil
}

// DeadFlagDesc returns a human description of dead flags.
func DeadFlagDesc(mask uint8) string {
	if mask == 0 {
		return ""
	}
	if mask == 0xFF {
		return "all"
	}
	s := ""
	flags := []struct {
		bit  uint8
		name string
	}{
		{0x01, "C"}, {0x02, "Z"}, {0x04, "I"}, {0x08, "D"},
		{0x40, "V"}, {0x80, "N"},
	}
	for _, f := range flags {
		if mask&f.bit != 0 {
			if s != "" {
				s += ","
			}
			s += f.name
		}
	}
	return s
}

func disasmSeq(seq []inst.Instruction) []string {
	result := make([]string, len(seq))
	for i, instr := range seq {
		result[i] = inst.Disassemble(instr)
	}
	return result
}

func joinSeq(seq []inst.Instruction) string {
	s := ""
	for i, instr := range seq {
		if i > 0 {
			s += " : "
		}
		s += inst.Disassemble(instr)
	}
	return s
}

func goStrSlice(seq []inst.Instruction) string {
	s := "[]string{"
	for i, instr := range seq {
		if i > 0 {
			s += ", "
		}
		s += fmt.Sprintf("%q", inst.Disassemble(instr))
	}
	s += "}"
	return s
}
